class Colword:
    def __init__(self, word, colour, birth, left, top, dir):
        # (str) Colour word 
        self.word = word
        # RGB code
        self.colour = colour
        # Time of birth
        self.birth = birth
        # Left pos
        self.left = left
        # Top pos
        self.top = top
        # Direction of motion in degrees
        self.dir = dir

